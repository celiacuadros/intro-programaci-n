/* COMPARACIONES
OPERADOR    DESCRIPCIÓN
==          Igual a...
!=          Diferente a...
>           Mayor a...
<           Menor a...
>=          Mayor o igual a...
<=          Menor o igual a...
===         Igal a... mismo tipo de dato */

/* Si comparamos valores o contenidos de variables, el resultado que devuelve es un valor booleano (true/false) */

Ejemplos: 
console.log(5 == 5);
var x = 10;
var y = 20;
console.log(x > y);

var DB_password = "1234";
